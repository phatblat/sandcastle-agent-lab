#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

export const SANDCASTLE_ENV_KEYS = [
  "CLAUDE_CODE_OAUTH_TOKEN",
  "ANTHROPIC_API_KEY",
  "OPENAI_API_KEY",
  "CODEX_ACCESS_TOKEN",
];

function parseEnvFile(text) {
  const values = new Map();
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const index = trimmed.indexOf("=");
    if (index === -1) continue;
    const key = trimmed.slice(0, index).trim();
    const raw = trimmed.slice(index + 1).trim();
    values.set(key, raw);
  }
  return values;
}

function formatEnvValue(value) {
  if (!value) return "";
  if (/^[A-Za-z0-9_./:@+-]+$/.test(value)) return value;
  return JSON.stringify(value);
}

export function ensureSandcastleEnv({
  cwd = process.cwd(),
  writeValues = process.env.SANDCASTLE_WRITE_ENV_VALUES === "1",
} = {}) {
  const envPath = join(cwd, ".sandcastle", ".env");
  mkdirSync(dirname(envPath), { recursive: true });

  let existing = new Map();
  try {
    existing = parseEnvFile(readFileSync(envPath, "utf8"));
  } catch (error) {
    if (error.code !== "ENOENT") throw error;
  }

  const lines = [
    "# Generated by scripts/seed-sandcastle-env.mjs.",
    "# This file is ignored by git.",
    "# Blank values make Sandcastle read matching host environment variables at launch.",
  ];

  for (const key of SANDCASTLE_ENV_KEYS) {
    const current = existing.get(key);
    const value = current !== undefined ? current : writeValues ? process.env[key] : "";
    lines.push(`${key}=${formatEnvValue(value)}`);
  }

  writeFileSync(envPath, `${lines.join("\n")}\n`);
  return { envPath, keys: SANDCASTLE_ENV_KEYS };
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  const result = ensureSandcastleEnv();
  console.log(`Prepared ${result.envPath}`);
  console.log(`Keys: ${result.keys.join(", ")}`);
}
